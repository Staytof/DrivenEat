//Function JS Button

const plateSection = document.getElementById("platesection");
const beerSection = document.getElementById("beersection");
const sobreSection = document.getElementById("sobresection");
const button = document.getElementById("button");
const button2 = document.getElementById("button2");

function checkAllSectionsSelected() {
    if (plateSection.classList.contains("selected") &&
        beerSection.classList.contains("selected") &&
        sobreSection.classList.contains("selected")) {
        button.style.display = "none";
        button2.style.display = "block";
    }
}

plateSection.addEventListener("click", function () {
    this.classList.toggle("selected");
    checkAllSectionsSelected();
});

beerSection.addEventListener("click", function () {
    this.classList.toggle("selected");
    checkAllSectionsSelected();
});

sobreSection.addEventListener("click", function () {
    this.classList.toggle("selected");
    checkAllSectionsSelected();
});
